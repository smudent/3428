function LightDisplay() {
    this.mainBackground = "#FFFFFF"
    this.buttonDark = "#E0E0E0"
    this.buttonLight = "#C0C0C0"
    this.textDark = "#303030"
    this.textLight = "#000000"
}

// Dark theme display. May need to be modified for more usage.
//
// Author Douglas Wetmore (A00395869)

function DarkDisplay() {
    this.mainBackground = "#202020"
    this.buttonDark = "#404040"
    this.buttonLight = "#505050"
    this.textDark = "#B0B0B0"
    this.textLight = "#E0E0E0"
}